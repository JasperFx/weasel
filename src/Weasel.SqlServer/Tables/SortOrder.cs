namespace Weasel.SqlServer.Tables;

/// <summary>
///     Specifies the direction used to sort items
/// </summary>
public enum SortOrder
{
    /// <summary>
    ///     Sorts in ascending order, from smallest to largest
    /// </summary>
    Asc,

    /// <summary>
    ///     Sorts in descending order, from largest to smallest
    /// </summary>
    Desc
}
